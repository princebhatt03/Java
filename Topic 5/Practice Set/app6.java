public class app6 {
    public static void main(String[] args) {
        System.out.println("Practice Set Question 4");
        System.out.println("Multiplication Table in Reverse Order");
        int num = 10;
        for (int i = num; i > 0; i--) {
            System.out.println(num + " X " + i + " = " + num * i);
        }
    }
}
